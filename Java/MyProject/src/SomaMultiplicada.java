public class SomaMultiplicada {
	public static void main(String[] args) {
		int primeiroNumero = 2;
		int segundoNumero = 3;
		int terceiroNumero = 4;
		
		int somaNumero = primeiroNumero + segundoNumero;
		int numeroMultiplicado = somaNumero * terceiroNumero;
		
		System.out.println("Primeiro número: " + primeiroNumero);
		System.out.println("Segundo número: " + segundoNumero);
		System.out.println("Terceiro número: " + terceiroNumero);
		
		System.out.println("A soma dos dois números multiplicado pelo terceiro número: " + numeroMultiplicado);

	}
}
